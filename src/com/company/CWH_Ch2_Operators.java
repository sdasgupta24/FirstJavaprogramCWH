package com.company;

public class CWH_Ch2_Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 6-a; // arithmetic operators
        int c = 6 % a; //modulo operator
        double d = 8.4 % 3.1; //decimal returning remainder
        a += 4 ;
        System.out.println(d);
        System.out.println(8<5); //comparison operators
        System.out.println(64>8 || 45>90); // logical operator
        System.out.println(2&3); //bitwise operator
    }

}
